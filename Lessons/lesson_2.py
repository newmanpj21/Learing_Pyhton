"""2 term addition calculator"""

# Get user input and store in variables

val1 = int(input("What is the first value: "))
val2 = int(input("What is the second value: "))

# add the values
total = val1 + val2


# print the sum out on the screen
print('The total is %s ' % total)

# this can also be done this way
print('The total is', total)

